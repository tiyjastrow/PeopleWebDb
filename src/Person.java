public class Person {

    String idNumber;
    String firstName;
    String lastName;
    String email;
    String country;
    String ipAddress;

    public Person(String idNumber, String firstName, String lastName, String email, String country, String ipAddress) {
        this.idNumber = idNumber;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.country = country;
        this.ipAddress = ipAddress;
    }
}
