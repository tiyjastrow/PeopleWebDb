public class Person {
    int id;
    String firstname;
    String lastname;
    String email;
    String country;
    String ip;

    public Person(int id, String firstname, String lastname, String email, String country, String ip) {
        this.id = id;
        this.firstname = firstname;
        this.lastname = lastname;
        this.email = email;
        this.country = country;
        this.ip = ip;
    }
}
